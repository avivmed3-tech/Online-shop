/* ---- deterministic badge / featured spread for items without explicit flags */
let i = 0;
for (const c of CATALOG) for (const s of c.subs) for (const it of s.items) {
  i++;
  if (it.badge === undefined && it.featured === undefined) {
    if (i % 8 === 3) it.badge = "מבצע";
    else if (i % 9 === 5) it.badge = "חדש";
    if (i % 7 === 2) it.featured = true;
  }
}

const q = (v) => v == null ? "null" : `'${String(v).replace(/'/g, "''")}'`;
const jb = (v) => `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`;

let sql = `-- Generated by generate_demo_seed.mjs — do not edit by hand.
-- Rich demo catalog: upserts by SKU (idempotent), images stay untouched so
-- real photos added later via the admin UI are never overwritten.
`;
let total = 0;
for (const c of CATALOG) {
  for (const s of c.subs) {
    const rows = s.items.map(it => {
      total++;
      return `  (${q(it.name)}, ${q(it.sku)}, ${q(c.cat)}, ${q(s.name)}, ${q(it.desc)}, ${it.price}, true, ${q(it.badge ?? null)}, ${it.featured ? "true" : "false"}, ${jb(it.vars ?? [])})`;
    }).join(",\n");
    sql += `
-- ${c.cat} › ${s.name}
insert into public.products
  (name, sku, category, subcategory, description, base_price, active, badge, featured, variants)
values
${rows}
on conflict (sku) where sku is not null and sku <> '' do update set
  name = excluded.name,
  category = excluded.category,
  subcategory = excluded.subcategory,
  description = excluded.description,
  base_price = excluded.base_price,
  badge = excluded.badge,
  featured = excluded.featured,
  variants = excluded.variants;
`;
  }
}

const out = join(dirname(fileURLToPath(import.meta.url)), "demo_catalog_seed.sql");
writeFileSync(out, sql);
console.log(`wrote ${out} — ${total} products`);
